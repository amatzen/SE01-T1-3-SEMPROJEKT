package dk.sdu.mmmi.swe20.t1.g3.Utilities;

/**
 * The type Scene location.
 */
public class SceneLocation {
    /**
     * The X.
     */
    int x;
    /**
     * The Y.
     */
    int y;

    /**
     * Instantiates a new Scene location.
     *
     * @param x the x
     * @param y the y
     */
    public SceneLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public int getY() {
        return y;
    }
}